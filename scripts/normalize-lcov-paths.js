/*
This script normalizes the paths in lcov.info files generated by Vitest.

It ensures that the paths are relative to the project root, which is necessary for proper coverage reporting.
It reads the lcov.info files from each project, modifies the paths to be relative to the project root, and writes the changes back to the file.

Example:
  SF:src/const.ts -> SF:packages/contract/analytics/src/const.ts
*/

const fs = require('fs');
const path = require('path');

const {
  createProjectGraphAsync,
  readProjectsConfigurationFromProjectGraph,
} = require('nx/src/project-graph/project-graph');

const main = async () => {
  const graph = await createProjectGraphAsync();
  const { projects } = readProjectsConfigurationFromProjectGraph(graph);

  for (const [name, config] of Object.entries(projects)) {
    const root = config.root;
    const lcovPath = path.join(root, 'coverage', 'lcov.info');

    if (!root || !fs.existsSync(lcovPath)) {
      console.warn(
        `❌ Skipping ${name}: ${!root ? 'no root' : 'no coverage file'}`,
      );
      continue;
    }

    let content = fs.readFileSync(lcovPath, 'utf8');

    content = content.replace(/^SF:(.+)$/gm, (_, fullPath) => {
      return `SF:${root}/${fullPath}`;
    });

    fs.writeFileSync(lcovPath, content);
    console.log(`✅ Normalized lcov.info for: ${name}, path: ${lcovPath}`);
  }
};

main()
  .then(() => {
    console.log('🏁 All coverage paths normalized.');
    process.exit(0);
  })
  .catch(error => {
    console.error('🔥 Error normalizing lcov paths:', error);
    process.exit(1);
  });
